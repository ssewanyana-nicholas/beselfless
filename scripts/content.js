// Counter Animation
document.addEventListener('DOMContentLoaded', function() {
    const counters = document.querySelectorAll('.counter');
    const speed = 200; // Animation speed - lower is faster

    const animateCounter = (counter) => {
        const target = +counter.getAttribute('data-count');
        let count = 0;
        
        const updateCount = () => {
            const increment = target / speed;
            
            if (count < target) {
                count += increment;
                counter.innerText = Math.ceil(count);
                setTimeout(updateCount, 1);
            } else {
                counter.innerText = target;
            }
        };

        updateCount();
    };

    // Intersection Observer to start counter when in view
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                animateCounter(entry.target);
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.5 });

    counters.forEach(counter => observer.observe(counter));
});

document.addEventListener('DOMContentLoaded', function() {
    // Counter values
    const counters = {
        count1: { target: 300, symbol: '+' },
        count2: { target: 90, symbol: '%' },
        count3: { target: 95, symbol: '%' },
        count4: { target: 85, symbol: '%' }
    };

    // Function to animate counter
    function animateCounter(elementId, target) {
        const element = document.getElementById(elementId);
        let current = 0;
        const increment = target / 50; // Will complete in 50 steps
        const timer = setInterval(() => {
            current += increment;
            if (current >= target) {
                current = target;
                clearInterval(timer);
            }
            element.textContent = Math.round(current);
        }, 40); // Update every 40ms
    }

    // Start all counters
    Object.keys(counters).forEach(counterId => {
        animateCounter(counterId, counters[counterId].target);
    });
});

