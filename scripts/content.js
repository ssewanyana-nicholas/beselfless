// Add custom animation for testimonial cards
function animateTestimonials() {
    $('.testimony-wrap').each(function(i) {
        var testimonial = $(this);
        setTimeout(function() {
            testimonial.addClass('animated fadeInUp');
        }, i * 100);
    });
}

// Initialize animations when section comes into view
$(window).scroll(function() {
    var testimonialSection = $('.testimony-section');
    if (testimonialSection.length) {
        var top_of_element = testimonialSection.offset().top;
        var bottom_of_element = testimonialSection.offset().top + testimonialSection.outerHeight();
        var bottom_of_screen = $(window).scrollTop() + $(window).innerHeight();
        var top_of_screen = $(window).scrollTop();

        if ((bottom_of_screen > top_of_element) && (top_of_screen < bottom_of_element)) {
            animateTestimonials();
        }
    }
});

